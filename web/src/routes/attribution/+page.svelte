	<h2>出典表示の原則</h2>
	<p>
		本サービスは、各記録に出典と取得時刻を付し、画面上で常に表示します。差分（変更点）は、周辺の文脈と全文へのリンクとともに提示し、切り取られた断片だけを結論として示すことはしません。
	</p>

	<h2>国会会議録（国立国会図書館）</h2>
	<p>
		会議録データベース自体、および国立国会図書館職員（館長を含む）の発言の著作権は、<strong
			>国立国会図書館（NDL）に帰属</strong
		>します。その他の発言の著作権は各発言者に帰属しますが、著作権法第40条第1項（公開して行われた政治上の演説・陳述等の利用）をはじめとする権利制限規定に基づき利用します。いずれの場合も出典を明記します。
	</p>
	<p class="cite">出典：国会会議録検索システム（国立国会図書館）
https://kokkai.ndl.go.jp/
（会議録データベースの著作権は国立国会図書館に帰属。
 個々の発言は著作権法第40条第1項等に基づき利用）</p>

	<h2>法令（e-Gov 法令検索 / デジタル庁）</h2>
