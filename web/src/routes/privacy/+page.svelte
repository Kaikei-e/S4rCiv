	<h2>2. 取り扱う情報</h2>
	<h3>サイトを閲覧される方について</h3>
	<p>
		本サービスは、ログイン・会員登録・送信フォームを設けておらず、氏名・連絡先など特定の個人を識別する情報を取得しません。アクセスに伴って生じる
		IP アドレス等は、特定の個人を識別しない「<strong>個人関連情報</strong
		>」として扱い、第三者へ個人データとして提供することはありません。
	</p>
	<p>
		運営者は、サーバ側で接続元 IP アドレス等のアクセスログを保持しません。アクセスに関する情報の処理は、後述の Cloudflare
		の配信・解析の範囲にとどまります。
	</p>
	<h3>本サービスが収集・記録する公的記録について</h3>
	<p>
		本サービスの収集対象は、公開された公的一次記録（国会会議録・法令など）です。これらに含まれる公職者等の個人情報は、<strong
			>説明責任を負う公的主体に限り</strong
		>、公開された一次ソースの範囲・粒度のまま、出典を付して取り扱います。私人（政治資金の少額寄附者など）について、横断的な名寄せや検索可能なプロファイルの作成は行いません。
	</p>

